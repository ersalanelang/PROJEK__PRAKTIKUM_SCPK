global data2;
global data3;

set(handles.uipanel1,'visible','off');
set(handles.uipanel2,'visible','off');
set(handles.uipanel3,'visible','off');
set(handles.uipanel4,'visible','off');
set(handles.uipanel5,'visible','on');

if isequal(data1,1)
    data_kriteria = get(handles.uitable1,'data');
else
    data_kriteria = str2double(get(handles.uitable1,'data'));
end

if isequal(data2,1)
    bobot_atribut = get(handles.uitable2,'data');
else
    bobot_atribut = str2double(get(handles.uitable2,'data'));
end

if isequal(data3,1)
    bobot_kriteria = get(handles.uitable3,'data');
else
    bobot_kriteria = str2double(get(handles.uitable3,'data'));
end

edit1 = get(handles.edit1,'string');
edit2 = get(handles.edit2,'string');
edit3 = get(handles.edit3,'string');
edit4 = get(handles.edit4,'string');

[m n] = size(data_kriteria); %matriks  m x n dengan ukuran sebanyak variabel x (input)
R = zeros(m,n); %membuat matriks R, yang merupakan matriks kosong
Y = zeros(m,n); %membuat matriks Y, yang merupakan matriks kosong
for j=1:n
    if bobot_atribut(j) == 1 %statement untuk kriteria dengan atribut keuntungan
        R(:,j)=data_kriteria(:,j)./max(data_kriteria(:,j));
    else
        R(:,j)=min(data_kriteria(:,j))./data_kriteria(:,j);
    end
    set(handles.uitable4,'Data',R);
end

for i=1:m
    V(i)= sum(bobot_kriteria.*R(i,:));  
    set(handles.uitable5,'Data',V);
    [nilai,urutan]=max(V);
    set(handles.text18,'string',(nilai));
    if(urutan == 1)
        hasil = edit1;
        a = handles.im1;
        axes(handles.axes5);
        imshow(a);
    elseif (urutan == 2)
        hasil = edit2;
        a = handles.im2;
        axes(handles.axes5);
        imshow(a);
    elseif (urutan == 3)
        hasil = edit3;
        a = handles.im3;
        axes(handles.axes5);
        imshow(a);
    elseif (urutan == 4)
        hasil = edit4;
        a = handles.im4;
        axes(handles.axes5);
        imshow(a);
    end
    set(handles.text20,'string',(urutan));
    set(handles.text21,'string',(hasil));
end


